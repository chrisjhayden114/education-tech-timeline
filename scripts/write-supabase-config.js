#!/usr/bin/env node
/**
 * Writes supabase-config.js for Netlify (or local) from environment variables.
 *
 * Set in Netlify: Site settings → Environment variables
 *   SUPABASE_URL
 *   SUPABASE_ANON_KEY
 *
 * Run locally (optional):
 *   SUPABASE_URL=... SUPABASE_ANON_KEY=... node scripts/write-supabase-config.js
 */
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const out = path.join(root, 'supabase-config.js');

const url = (process.env.SUPABASE_URL || '').trim();
const key = (process.env.SUPABASE_ANON_KEY || '').trim();

const body = `// Generated by scripts/write-supabase-config.js — do not hand-edit on Netlify builds.
window.SUPABASE_URL = ${JSON.stringify(url)};
window.SUPABASE_ANON_KEY = ${JSON.stringify(key)};
`;

fs.writeFileSync(out, body);
if (url && key) {
  console.log('Wrote supabase-config.js with Supabase credentials (shared comments enabled).');
} else {
  console.log('Wrote supabase-config.js with empty credentials (demo / localStorage comments).');
  console.log('To enable shared comments, set SUPABASE_URL and SUPABASE_ANON_KEY.');
}
